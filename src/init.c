#include "ssl.h"

void	init_md5_struct(t_md5 *md5)
{
	md5->A = 0x67452301;
	md5->B = 0xefcdab89;
	md5->C = 0x98badcfe;
	md5->D = 0x10325476;
	md5->bit_len = 0;
	memset(md5->buffer, 0, 64);
}

